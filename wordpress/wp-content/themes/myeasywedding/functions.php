function dashboard_abonnent(){
    //Abonnent = subscriber
    if(current_user_can( 'subscriber' )){
        
    global $menu;
		$restricted = array(__('Dashboard'),  __('Links'), __('Seiten'), __('Kommentare'), __('Design'), __('Plugins'), __('Benutzer'), __('Werkzeuge'), __('Einstellungen'));
		end ($menu);
		while (prev($menu)){
			$value = explode(' ',$menu[key($menu)][0]);
			if(in_array($value[0] != NULL?$value[0]:"" , $restricted)){unset($menu[key($menu)]);}
		}
      
    }
        

}
    
